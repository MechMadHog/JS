/* UpperCase & LowerCase: */
var upperSample = "make me big";
document.write(upperSample.toUpperCase());
// Output will be: MAKE ME BIG

var lowerSample = "MAKE ME SMALL";
document.write(lowerSample.toLowerCase());
// Output will be: make me small

