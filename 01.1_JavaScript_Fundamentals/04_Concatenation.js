// Concatenation:
// This is the ability to COMBINE Variables and or Strings WITHOUT adding them together.
var Name = "Michael";
document.write("My name is " + Name + ".");
// This will output: My name is Michael.
// Spaces MUST be placed INSIDE of a string, Example: " ".

// Other Example:
var Name = "Michael";
var Age = 29;
document.write("My name is " + Name + " and I am " + Age + " years old.");
// ONLY numbers  can be place OUTSIDE of quotes.
// Although adding two INTs (integers) together would result in the SUM of the TWO figures.
// Variables are NOT CALLED inside of quotations.
