// Switch Statements:
//The code that is executed is ENTIRELY based on the answer that is provided.
var name = "Michael";
switch (name /* The switch variable */) {
  case "Chris": // if
    alert("Hello, Chris!");
    break;
  case "Ardan": // else if
    alert("Hello, Ardan!");
    break;
  case "Michael": // else if
    alert("Hello, Michael!"); //What I'm Actually Looking For.
    break;
  /* else */ default:
    alert("The name Variable Doesn't Match Any Name Above? ");
    break;
}
// Switch can use as many cases as necessary.
